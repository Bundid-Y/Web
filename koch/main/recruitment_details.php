        (function () {
            const params = new URLSearchParams(window.location.search);
            const selectedJob = params.get('job') || 'truck-mechanic';
            const jobs = document.querySelectorAll('.job-detail');
            const links = document.querySelectorAll('.position-links a');
            const selectedPanel = document.getElementById(selectedJob) || document.getElementById('truck-mechanic');
            const heroTitle = document.getElementById('hero-title');

            jobs.forEach((job) => {
                job.hidden = job !== selectedPanel;
            });

            links.forEach((link) => {
                const isActive = link.dataset.target === selectedPanel.id;
                link.classList.toggle('is-active', isActive);
                if (isActive) {
                    link.setAttribute('aria-current', 'page');
                }
            });

            if (selectedPanel && heroTitle) {
                heroTitle.textContent = selectedPanel.dataset.title;
            }
        })();
